$(document).ready(function(){
	$('.content_toggle').click(function(){
		$('.content_block').slideToggle(300);
		return false;
	});
});
	$('.box_black').click(function(){
		$('.content_block').slideToggle(300);
		return false;
	});
  $('.content_toggle').on("click", function() {
    $(".box_black").toggleClass('ordered');
    $(".content_toggle").toggleClass('underline');
});
  $('.box_black').on("click", function() {
    $(".box_black").toggleClass('ordered');
      $(".content_toggle").toggleClass('underline');
});
$('.order_window').on("click", function() {
  $(".order_window").addClass('ordered');
});
$('.order_window').click(function(){
  	$(this).html('ЗАБРОНИРОВАННО');
});


